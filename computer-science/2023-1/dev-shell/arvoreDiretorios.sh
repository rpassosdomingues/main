#!/bin/bash

# Cria o diretório principal
mkdir projeto

# Cria os subdiretórios
mkdir projeto/img
mkdir projeto/audio
mkdir projeto/video

# Cria os arquivos de exemplo
touch projeto/img/imagem1.jpg
touch projeto/audio/audio1.mp3
touch projeto/video/video1.mp4

# Cria os arquivos HTML
touch projeto/index.html
touch projeto/video.html
touch projeto/audio.html

echo "Árvore de diretórios criada com sucesso!"
